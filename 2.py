# Program 2: print statements with end and sep
print('My name is', 'Logan.') # single quotes also work
print('Logan Mulvey.')
print() # an empty print statement adds a blank line
print('My name is', 'Logan.', end=' ') # end adds a string at the end of every strng involved
print('Logan Mulvey.')

print('My', 'name', 'is', 'Logan.', sep=' - ') # sep adds a string between each string

print('My birthday is', end='...')
print('6', '19', '2005', sep='/')