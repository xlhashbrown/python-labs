# Program 2: print statements with end and sep
print('My name is', 'L.') # single quotes also work
print('L.')
print() # an empty print statement adds a blank line
print('My name is', 'L.', end=' ') # end adds a string at the end of every strng involved
print('Logan Mulvey.')

print('My', 'name', 'is', 'L.', sep=' - ') # sep adds a string between each string

print('My birthday is', end='...')
print('X', 'XX', 'XXXX', sep='/')
