# Program 10: shortcut operators
x = 8
x = x + 2
print(x)

x = 8
x += 2
print(x) # += is shorthand for addition

x = 6
x = x * 3
print(x)

x = 6
x *= 3
print(x) # *= is shorthand for multiplacation

x = 15
x = x - 2
print(x)

x = 15
x -= 2
print(x) # -= is shorthand for subtraction
# the rest of the shorthands are self-explanitory